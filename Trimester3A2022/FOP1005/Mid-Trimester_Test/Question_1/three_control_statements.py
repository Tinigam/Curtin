# The if statement is used to execute code if a condition is true.
condition = True
if condition is True:
    print("This is true")
else:
    print("This is false")
# The while statement is used to execute code repeatedly while a condition is true.
i = 1
while i <= 5:
    print(i, end = ", ")
    i += 1
print()
# The for statement is used to iterate over a sequence of elements.
for x in [1, 2, 3, 4, 5]:
    print(x, end = ", ")
print()